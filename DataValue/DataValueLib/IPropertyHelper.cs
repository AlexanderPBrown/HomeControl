namespace DataValueLib
{
    public interface IPropertyHelper
    {
        #region Methods

        /// <summary>
        /// Returns the value for the property with the specified name.
        /// </summary>
        /// <param name="property">The property name.</param>
        /// <returns>The property value.</returns>
        object GetPropertyValue(string property);

        /// <summary>
        /// Sets the value for the property with the specified name.
        /// </summary>
        /// <param name="property">The property name.</param>
        /// <param name="value">The property value.</param>
        void SetPropertyValue(string property, object value);

        #endregion
    }
}